    private void btnCreerStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreerStockActionPerformed
     StockDAO stockDAO = new StockDAO();
     Stock s = new Stock();
     String type ="";
     if (radBtnF.isSelected())
     {
         type = radBtnF.getText();
     }else
         if (radBtnH.isSelected()) {
            type = radBtnH.getText();
        }
     s.setType_vet_stock(type);
     
     int qte1 = Integer.parseInt(qte.getValue().toString());
     s.setNombre_articles(qte1);
     String deptadresse = ComboBoxStock.getSelectedItem().toString();
     
     DepotDAO depotDAO = new DepotDAO();
     Depot depot = depotDAO.findDepotByAdresse(deptadresse);
     s.setDepot(depot);
     stockDAO.insertStock(s);
     
     JOptionPane.showMessageDialog(rootPane, "Creation reussi ");
     
     
   
     
    }//GEN-LAST:event_btnCreerStockActionPerformed

